# DICTIONARY
    # Works exactly like a dictionary
    # aka hashes or object in JavaScript
    # It has key and values pairs
        # the key is the word you look up
        # the value is the meaning / value of the word

# Syntax
# dict = {'key': 'value'
# Define a variable called my_dictionary
my_dictionary = {'tissues': 'a disposable piece of absorbent paper', 'baseball_bat': 'bat for playing baseball'}

# Accessing or re-assigning is with the key inside the []
print(my_dictionary['baseball_bat'])

my_dictionary['baseball_bat'] = 'Google it'
print(my_dictionary['baseball_bat'])
